package com.server2.content.constants;

/**
 * @author Rene A class with constant time variables holding times like hours in
 *         MS for easy use
 */

public class TimingConstants {

	/**
	 * A day(24 Hour period)
	 */
	public static final int DAY = 86400000;

	/**
	 * An hour
	 */
	public static final int HOUR = 3600000;

	/**
	 * A minute - 60000MS
	 */
	public static final int MINUTE = 60000;

	/**
	 * A second
	 */
	public static final int SECOND = 1000;

	/**
	 * One game tick
	 */
	public static final int tick = 600;
}